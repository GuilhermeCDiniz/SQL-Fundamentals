SELECT Gender, COUNT(Gender) as COUNTGENDER
FROM EMPLOYEE
WHERE Gender IS NOT NULL
GROUP BY Gender
ORDER BY COUNTGENDER DESC;

SELECT JobTitle, COUNT(JobTitle) AS QTD
FROM EMPLOYEE
GROUP BY JobTitle
HAVING COUNT(JobTitle) > 1;

SELECT A.JobTitle, SUM(B.Salary)
FROM EMPLOYEE A
INNER JOIN EMPLOYEESALARY B
ON A.EmployeeID = B.EmployeeID
GROUP BY JobTitle
HAVING SUM(B.Salary) > 60000;

SELECT A.JobTitle, AVG(B.Salary)
FROM EMPLOYEE A
INNER JOIN EMPLOYEESALARY B
ON A.EmployeeID = B.EmployeeID
GROUP BY JobTitle
HAVING AVG(B.Salary) > 45000;
